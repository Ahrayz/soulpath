import {
  matchArchetype,
  getTopTraits,
  getSoulEmoji,
  getDominantVibe,
  SELF_ARCHETYPES,
  AI_ARCHETYPES,
  generateNameSuggestions,
} from './personalities'

/**
 * Compute archetypes and guideline data from quiz answers.
 * This is the local computation step — does not call any remote model.
 */
export function computeQuizResults(selfTraitScores, aiTraitScores) {
  const selfArchetype = matchArchetype(selfTraitScores, SELF_ARCHETYPES)
  const aiArchetype = matchArchetype(aiTraitScores, AI_ARCHETYPES)
  const soulEmoji = getSoulEmoji(selfArchetype.key, aiArchetype.key)
  const dominantVibe = getDominantVibe(aiTraitScores)
  const selfTopTraits = getTopTraits(selfTraitScores, 3)
  const aiTopTraits = getTopTraits(aiTraitScores, 3)
  const nameSuggestions = generateNameSuggestions(selfArchetype, aiArchetype)

  // Build guideline strings for model prompts
  const userGuideline = buildUserGuideline(selfArchetype, selfTopTraits, aiArchetype)
  const soulGuideline = buildSoulGuideline(aiArchetype, dominantVibe, selfArchetype, soulEmoji, selfTopTraits)

  return {
    soulEmoji,
    selfArchetype,
    aiArchetype,
    selfTopTraits,
    aiTopTraits,
    dominantVibe,
    nameSuggestions,
    userGuideline,
    soulGuideline,
  }
}

/**
 * Build the user guideline string for model prompt generation
 */
function buildUserGuideline(selfArchetype, selfTopTraits, aiArchetype) {
  const traitNames = selfTopTraits.map(t => t.trait)
  return [
    `Human personality archetype: ${selfArchetype.name} ${selfArchetype.emoji}`,
    `Core traits: ${traitNames.join(', ')}`,
    `Keywords: ${selfArchetype.keywords.join(', ')}`,
    `Description: ${selfArchetype.description}`,
    `The user values: ${traitNames.map(t => traitDescriptions[t] || t).join('; ')}`,
    `Matched AI companion: ${aiArchetype.name} (${aiArchetype.creature}, ${aiArchetype.style.join(', ')})`,
    `Relationship dynamic: A ${selfArchetype.keywords[0]} human paired with a ${aiArchetype.creature} — think about why this pairing works and what each side brings to the other.`,
  ].join('\n')
}

/**
 * Build the soul guideline string for model prompt generation
 */
function buildSoulGuideline(aiArchetype, dominantVibe, selfArchetype, soulEmoji, selfTopTraits) {
  const humanTraits = selfTopTraits.map(t => t.trait)
  return [
    `AI companion archetype: ${aiArchetype.name} ${aiArchetype.emoji}`,
    `Creature type: ${aiArchetype.creature}`,
    `Vibe: ${dominantVibe}`,
    `Communication style: ${aiArchetype.style.join(', ')}`,
    `Description: ${aiArchetype.description}`,
    `Paired with human archetype: ${selfArchetype.name} (${selfArchetype.keywords.join(', ')})`,
    `Human's core traits: ${humanTraits.join(', ')}`,
    `Soul emoji: ${soulEmoji}`,
    `Relationship insight: This soul was born from its human's personality. A ${humanTraits[0] || 'curious'} human chose a ${dominantVibe} ${aiArchetype.creature} — explore what that says about them and how this soul should complement, challenge, or ground its human.`,
  ].join('\n')
}

const traitDescriptions = {
  warmth: 'genuine connection and emotional warmth',
  logic: 'logical reasoning and structured thinking',
  creativity: 'creative thinking and unconventional approaches',
  courage: 'boldness and ambitious goals',
  calm: 'measured, peaceful problem-solving',
  curiosity: 'exploring new ideas and deep learning',
  empathy: 'emotional intelligence and sensitivity',
  humor: 'lightness, humor, and not taking things too seriously',
}

