/**
 * Validate a generated markdown file
 */
export function validateSoulMd(markdown) {
  const checks = {
    hasTitle: /^#\s.+/m.test(markdown),
    hasCoreTruths: /## Core Truths/m.test(markdown) || /## Context/m.test(markdown),
    hasBoundaries: /## Boundaries/m.test(markdown) || /## Preferences/m.test(markdown),
    hasVibe: /## Vibe/m.test(markdown) || /## AI Companion/m.test(markdown),
    hasEmoji: /[\u{1F300}-\u{1FEFF}]/u.test(markdown),
    isNotEmpty: markdown.trim().length > 100,
  }
  const allValid = Object.values(checks).every(Boolean)
  return { valid: allValid, checks }
}

export function validateUserMd(markdown) {
  const checks = {
    hasTitle: /^#\s.+/m.test(markdown),
    hasNameField: /Name:/m.test(markdown),
    hasContext: /## Context/m.test(markdown) || /## Preferences/m.test(markdown),
    isNotEmpty: markdown.trim().length > 50,
  }
  const allValid = Object.values(checks).every(Boolean)
  return { valid: allValid, checks }
}
