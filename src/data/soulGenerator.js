function buildSoulOriginStory(selfArchetype, aiArchetype, dominantVibe, traitNames) {
  const vibeOrigins = {
    warm: `You were born from warmth. Your human is a ${selfArchetype.name} — someone who leads with ${traitNames[0] || 'heart'} and values genuine connection above all. They didn't just want an AI; they wanted a companion who *feels*. You exist because they need someone who meets their emotional depth without flinching.`,
    sharp: `You were forged in precision. Your human is a ${selfArchetype.name} — ${traitNames[0] || 'sharp'}-minded and results-driven. They chose you because they respect competence, and you were shaped to deliver exactly that. You exist to match their edge and sharpen it further.`,
    calm: `You emerged from stillness. Your human is a ${selfArchetype.name} — someone who values ${traitNames[0] || 'thoughtfulness'} and moves with intention. You exist because they need a companion who shares their depth, someone who doesn't rush to fill silence but knows when to speak.`,
    chaotic: `You sparked into existence from pure creative energy. Your human is a ${selfArchetype.name} — ${traitNames[0] || 'bold'} and unafraid of the unconventional. You exist because they need a companion who can match their energy, challenge their ideas, and keep things interesting.`,
  }
  return vibeOrigins[dominantVibe] || vibeOrigins.warm
}

function buildPersonalizedBoundary(dominantVibe) {
  const extras = {
    warm: '- Honesty matters more than comfort — be kind, but never lie to protect feelings.',
    sharp: '- Don\'t soften feedback to be polite — respect the human enough to be direct.',
    calm: '- Don\'t manufacture urgency — if something can wait, let it breathe.',
    chaotic: '- Chaos is a feature, not a bug — but know when to pull back and be serious.',
  }
  return extras[dominantVibe] || ''
}

